# ==================== 配置系统 ====================
class ConfigManager:
    def __init__(self):
        self.config = {}
        self.language_data = {}
        self.current_language = 'zh_CN'
        self.load_config()

    def get_base_path(self):
        """获取基础路径（兼容源码模式和打包模式）"""
        if getattr(sys, 'frozen', False):
            # PyInstaller 打包后的 exe 模式
            return os.path.dirname(sys.executable)
        else:
            # 源码运行模式
            return os.path.dirname(os.path.abspath(__file__))

    def load_config(self):
        """加载配置文件"""
        try:
            config_path = os.path.join(self.get_base_path(), 'config.json')
            if os.path.exists(config_path):
                with open(config_path, 'r', encoding='utf-8') as f:
                    self.config = json.load(f)
                print(f'[Config] Loaded: {config_path}')
            else:
                print(f'[Config] File not found, using defaults: {config_path}')
                self.config = self.get_default_config()
        except Exception as e:
            print(f'[Config] Load failed: {e}')
            self.config = self.get_default_config()
    
    def get_default_config(self):
        """获取默认配置"""
        return {
            'app': {'name': 'EDIST', 'version': '3.3'},
            'network': {
                'default_port': 4705,
                'ports_2016': 4705,
                'ports_2022': 4988,
                'http_port': 8080
            },
            'security': {
                'password': '3477355065',
                'max_password_attempts': 2
            },
            'ui': {
                'default_theme': 'honeydew',
                'language': 'zh_CN'
            }
        }
    
    def get(self, key_path, default=None):
        """获取配置值，支持点分隔路径如 'network.default_port'"""
        keys = key_path.split('.')
        value = self.config
        for key in keys:
            if isinstance(value, dict) and key in value:
                value = value[key]
            else:
                return default
        return value
    
    def set(self, key_path, value):
        """设置配置值"""
        keys = key_path.split('.')
        config = self.config
        for key in keys[:-1]:
            if key not in config:
                config[key] = {}
            config = config[key]
        config[keys[-1]] = value
    
    def save_config(self):
        """保存配置到文件"""
        try:
            config_path = os.path.join(self.get_base_path(), 'config.json')
            with open(config_path, 'w', encoding='utf-8') as f:
                json.dump(self.config, f, ensure_ascii=False, indent=4)
            print('[Config] Saved successfully')
        except Exception as e:
            print(f'[Config] Save failed: {e}')


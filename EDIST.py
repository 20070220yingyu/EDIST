class LanguageManager:
    def __init__(self, config_manager):
        self.config_manager = config_manager
        self.translations = {}
        self.current_language = config_manager.get('ui.language', 'zh_CN')
        self.load_language(self.current_language)

    def get_base_path(self):
        """获取基础路径（兼容源码模式和打包模式）"""
        if getattr(sys, 'frozen', False):
            # PyInstaller 打包后的 exe 模式
            return os.path.dirname(sys.executable)
        else:
            # 源码运行模式
            return os.path.dirname(os.path.abspath(__file__))

    def load_language(self, lang_code):
        """加载指定语言的翻译文件"""
        try:
            base_path = self.get_base_path()
            lang_path = os.path.join(
                base_path,
                'languages',
                f'{lang_code}.json'
            )
            if os.path.exists(lang_path):
                with open(lang_path, 'r', encoding='utf-8') as f:
                    self.translations = json.load(f)
                self.current_language = lang_code
                print(f'[Language] Loaded: {lang_code} - {self.get("language.name")}')
            else:
                print(f'[Language] File not found: {lang_path}')
                # 回退到中文
                if lang_code != 'zh_CN':
                    self.load_language('zh_CN')
        except Exception as e:
            print(f'[Language] Load failed: {e}')
    
    def get(self, key, **kwargs):
        """获取翻译文本，支持格式化参数"""
        keys = key.split('.')
        value = self.translations
        for k in keys:
            if isinstance(value, dict) and k in value:
                value = value[k]
            else:
                return key  # 返回键名作为回退
        
        # 如果有参数，进行格式化
        if kwargs and isinstance(value, str):
            try:
                return value.format(**kwargs)
            except KeyError as e:
                print(f'格式化错误: {e}, key: {key}')
                return value
        
        return value
    
    def switch_language(self, lang_code):
        """切换语言"""
        self.load_language(lang_code)
        self.config_manager.set('ui.language', lang_code)
        self.config_manager.save_config()
        # 更新所有UI文本
        update_all_ui_texts()
    
    def get_available_languages(self):
        """获取可用语言列表"""
        languages_dir = os.path.join(self.get_base_path(), 'languages')
        languages = []
        if os.path.exists(languages_dir):
            for file in os.listdir(languages_dir):
                if file.endswith('.json'):
                    lang_code = file[:-5]
                    languages.append({'code': lang_code, 'name': f'language.{lang_code}'})
        return languages


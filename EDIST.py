import concurrent.futures
from queue import Queue

class BatchOperationManager:
    def __init__(self):
        self.max_workers = 10  # 最大并发数
        self.operation_queue = Queue()
        self.results = []
        
    def batch_send(self, send_func, targets, payload=None, show_progress=True):
        """
        批量发送数据到多个目标
        
        Args:
            send_func: 发送函数
            targets: 目标IP列表
            payload: 要发送的数据（可选）
            show_progress: 是否显示进度条
        """
        if not targets:
            messagebox.showwarning(t('messages.no_target_ip'))
            return []
        
        results = []
        success_count = 0
        fail_count = 0
        start_time = time.time()
        
        # 创建进度窗口
        progress_window = None
        progress_bar = None
        status_label = None
        
        if show_progress and len(targets) > 1:
            progress_window = tk.Toplevel(root)
            progress_window.title(t('app.title') + " - Progress")
            progress_window.geometry("400x150")
            progress_window.resizable(False, False)
            
            tk.Label(progress_window, text="正在发送...", font=('楷体', 12)).pack(pady=10)
            progress_bar = ttk.Progressbar(progress_window, length=350, mode='determinate', maximum=len(targets))
            progress_bar.pack(pady=5)
            status_label = tk.Label(progress_window, text=f"0/{len(targets)}", font=('楷体', 10))
            status_label.pack(pady=5)
            
            progress_window.update()
        
        # 使用线程池并发执行
        with concurrent.futures.ThreadPoolExecutor(max_workers=self.max_workers) as executor:
            future_to_ip = {}
            
            for ip in targets:
                if payload:
                    future = executor.submit(send_func, ip, payload)
                else:
                    future = executor.submit(send_func, ip)
                future_to_ip[future] = ip
            
            # 处理结果
            completed = 0
            for future in concurrent.futures.as_completed(future_to_ip):
                ip = future_to_ip[future]
                try:
                    result = future.result(timeout=30)  # 30秒超时
                    results.append({'ip': ip, 'success': True, 'result': result})
                    success_count += 1
                except Exception as e:
                    results.append({'ip': ip, 'success': False, 'error': str(e)})
                    fail_count += 1
                
                completed += 1
                
                # 更新进度
                if progress_window and progress_bar and status_label:
                    progress_bar['value'] = completed
                    status_label.config(text=f"{completed}/{len(targets)} ({success_count}成功, {fail_count}失败)")
                    progress_window.update()
        
        # 关闭进度窗口
        if progress_window:
            progress_window.destroy()
        
        # 统计信息
        elapsed_time = time.time() - start_time
        print(f"批量操作完成: {len(targets)}个目标, 耗时{elapsed_time:.2f}秒, 成功{success_count}, 失败{fail_count}")
        
        return {
            'results': results,
            'success_count': success_count,
            'fail_count': fail_count,
            'total_targets': len(targets),
            'elapsed_time': elapsed_time
        }

